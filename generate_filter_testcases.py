def write_section(ws, start_row, title, headers, data_rows):
    ws.cell(row=start_row, column=1, value=title).font = section_font
    ws.cell(row=start_row, column=1).fill = section_fill
    for ci in range(1, len(headers)+1):
        ws.cell(row=start_row, column=ci).fill = section_fill
    hdr_row = start_row + 1
    for ci, h in enumerate(headers, 1):
        cell = ws.cell(row=hdr_row, column=ci, value=h)
        cell.font = header2_font
        cell.border = thin_border
    for ri, row in enumerate(data_rows):
        for ci, val in enumerate(row, 1):
            cell = ws.cell(row=hdr_row+1+ri, column=ci, value=val)
            cell.border = thin_border
    return hdr_row + 1 + len(data_rows) + 2

